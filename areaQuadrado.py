#Faça um Programa que calcule a área de um
#quadrado, em seguida mostre o dobro desta área
#para o usuário.
altura = float(input('Insira a altura do quadrado(em centímetros): '))
base = float(input('Insira a base do quadrado(em centímetros); '))

area = base * altura
dobroArea = area * 2
print('O dobro da area do quadrado é: ',dobroArea)